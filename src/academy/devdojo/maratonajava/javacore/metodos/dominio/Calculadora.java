public class Calculadora {
    public void somaDoisNumeros(int numberOne, int numberTwo) {
        int result = numberOne + numberTwo;
        System.out.println("A soma dos numeros = " + result);
    }

    public void subtraiDoisNumeros(int numberOne, int numberTwo) {
        int result = numberOne - numberTwo;
        System.out.println("A subtrai dos numeros = " + result);
    }
}
